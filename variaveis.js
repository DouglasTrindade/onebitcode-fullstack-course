// Variaveis servem para armazenar valores dentro delas
// Ex: 
let name = 'Douglas'
var age = 29

alert(`${name} tem ${age} anos de idade`);
//Alteração de valor da variável
//Ex: 

name = 'Artur';
age = 30

alert(`${name} tem ${age} anos de idade`);

// As variáveis LET e VAR podem ser alterados os valores.
// A variável CONST não pode ser alterado, pois é uma CONSTANTE